def load_bootstrap_data() -> cudf.DataFrame:
    """Loads and prepares the bootstrap data with consistent types."""
    try:
        logging.info("Loading bootstrap TD and TMIN data from December 2016...")
        td_file = os.path.join(SOURCE_DATA_PATH, "td/Outputs", "td_daily_2016_12.parquet")
        tmin_file = os.path.join(SOURCE_DATA_PATH, "tmin/Outputs", "tmin_daily_2016_12.parquet")

        td_gdf = cudf.read_parquet(td_file).rename(columns={'Value': 'TD'}).drop(columns=['source_file'], errors='ignore')
        tmin_gdf = cudf.read_parquet(tmin_file).rename(columns={'Value': 'TMIN'}).drop(columns=['source_file'], errors='ignore')

        td_gdf['FECHA'] = td_gdf['FECHA'].astype('datetime64[ns]')
        tmin_gdf['FECHA'] = tmin_gdf['FECHA'].astype('datetime64[ns]')

        bootstrap_gdf = cudf.merge(td_gdf, tmin_gdf, on=['ID', 'FECHA'], how='inner')

        last_days = pd.to_datetime(['2016-12-30', '2016-12-31'])
        bootstrap_gdf = bootstrap_gdf[bootstrap_gdf['FECHA'].isin(last_days)]

        return bootstrap_gdf.astype(DTYPE_MAP).sort_values(['ID', 'FECHA']).reset_index(drop=True)
    except Exception as e:
        logging.critical("Failed during bootstrap data preparation!", exc_info=True)
        raise

def create_doy_mapping(start_date, end_date) -> cudf.DataFrame:
    """Creates a mapping to handle leap years correctly."""
    logging.info("Creating leap-year-aware DOY mapping...")
    df = pd.DataFrame({'FECHA': pd.date_range(start_date, end_date, freq='D')})
    df['model_doy'] = df['FECHA'].dt.dayofyear
    non_leap_year_mask = ~df['FECHA'].dt.is_leap_year & (df['model_doy'] >= 60)
    df.loc[non_leap_year_mask, 'model_doy'] += 1
    df['FECHA'] = df['FECHA'].astype('datetime64[ns]')
    return cudf.from_pandas(df)

def main():
    """Main pipeline for day-by-day forecasting with all fixes integrated."""
    try:
        history_df = load_bootstrap_data()
        start_date = pd.Timestamp(f'{FORECAST_YEARS[0]}-01-01')
        end_date = pd.Timestamp(f'{FORECAST_YEARS[1]}-12-31')
        doy_map = create_doy_mapping(start_date, end_date)

        monthly_results = []
        loaded_tmin_data = None
        loaded_month_str = ""

        for day_to_forecast in tqdm(pd.date_range(start_date, end_date), desc="Forecasting Day-by-Day"):
            current_month_str = day_to_forecast.strftime('%Y_%m')

            if current_month_str != loaded_month_str:
                tmin_file = os.path.join(SOURCE_DATA_PATH, "tmin/Outputs", f"tmin_daily_{current_month_str}.parquet")
                if not os.path.exists(tmin_file):
                    logging.warning(f"TMIN file not found: {tmin_file}. Skipping month.")
                    continue
                loaded_tmin_data = cudf.read_parquet(tmin_file).rename(columns={'Value': 'TMIN'})
                loaded_tmin_data['FECHA'] = loaded_tmin_data['FECHA'].astype('datetime64[ns]')
                loaded_tmin_data = loaded_tmin_data.astype({'ID': 'int32', 'TMIN': 'float32'})
                loaded_month_str = current_month_str

            current_tmin = loaded_tmin_data[loaded_tmin_data['FECHA'] == day_to_forecast]
            if current_tmin.empty: continue

            day_minus_1 = day_to_forecast - pd.Timedelta(days=1)
            day_minus_2 = day_to_forecast - pd.Timedelta(days=2)

            td_lag1 = history_df[history_df['FECHA'] == day_minus_1][['ID', 'TD']].rename(columns={'TD': 'TD_lag1'})
            td_lag2 = history_df[history_df['FECHA'] == day_minus_2][['ID', 'TD']].rename(columns={'TD': 'TD_lag2'})
            tmin_lag1 = history_df[history_df['FECHA'] == day_minus_1][['ID', 'TMIN']].rename(columns={'TMIN': 'TMIN_lag1'})

            features_df = current_tmin.merge(td_lag1, on='ID', how='left').merge(td_lag2, on='ID', how='left').merge(tmin_lag1, on='ID', how='left')
            features_df = features_df.merge(doy_map, on='FECHA', how='left')

            required_doys = features_df['model_doy'].dropna().unique().to_arrow().to_pylist()
            if not required_doys: continue

            model_files = [os.path.join(MODELS_PATH, f"gpu_models_doy_{int(d)}.parquet") for d in required_doys if os.path.exists(os.path.join(MODELS_PATH, f"gpu_models_doy_{int(d)}.parquet"))]
            if not model_files: continue

            model_gdf = cudf.read_parquet(model_files).rename(columns={'doy': 'model_doy', 'TMIN': 'tmin_coeff', 'TD_lag1': 'td1_coeff', 'TD_lag2': 'td2_coeff', 'TMIN_lag1': 'tmin1_coeff'})

            predict_df = features_df.merge(model_gdf, on=['ID', 'model_doy'], how='left')

            predict_df['TD'] = cudf.NA
            valid_mask = predict_df[['const', 'TMIN', 'TD_lag1', 'TD_lag2', 'TMIN_lag1']].notna().all(axis=1)

            predict_df.loc[valid_mask, 'TD'] = (
                predict_df.loc[valid_mask, 'const'] +
                (predict_df.loc[valid_mask, 'tmin_coeff'] * predict_df.loc[valid_mask, 'TMIN']) +
                (predict_df.loc[valid_mask, 'td1_coeff'] * predict_df.loc[valid_mask, 'TD_lag1']) +
                (predict_df.loc[valid_mask, 'td2_coeff'] * predict_df.loc[valid_mask, 'TD_lag2']) +
                (predict_df.loc[valid_mask, 'tmin1_coeff'] * predict_df.loc[valid_mask, 'TMIN_lag1'])
            )

            predict_df['TD'] = predict_df['TD'].astype('float32')

            todays_forecast = predict_df[['ID', 'FECHA', 'TMIN', 'TD']]
            monthly_results.append(todays_forecast[['ID', 'FECHA', 'TD']])

            history_df = cudf.concat([history_df.astype(DTYPE_MAP), todays_forecast.astype(DTYPE_MAP)])

            three_days_ago = day_to_forecast - pd.Timedelta(days=3)
            history_df = history_df[history_df['FECHA'] > three_days_ago]

            if day_to_forecast.is_month_end:
                logging.info(f"Saving forecasts for {day_to_forecast.strftime('%Y-%m')}...")
                month_df = cudf.concat(monthly_results).rename(columns={'TD': 'Value'})

                # --- CORRECTED SAVING LOGIC ---
                # This guarantees a single file is written
                output_filename = os.path.join(FORECASTS_PATH, f"td_forecast_{day_to_forecast.strftime('%Y-%m')}.parquet")

                # Convert to an Arrow Table and write with pyarrow's native writer
                arrow_table = month_df.to_arrow()
                pq.write_table(arrow_table, output_filename)

                monthly_results = []
                gc.collect()

    except Exception as e:
        logging.critical("Forecasting pipeline failed.", exc_info=True)

if __name__ == "__main__":
    main()

