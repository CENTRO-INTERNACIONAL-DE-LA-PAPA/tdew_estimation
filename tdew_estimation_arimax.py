def fit_one_model(df_group: cudf.DataFrame, doy_target: int, h: int) -> cudf.DataFrame:
    """
    Fit a weighted linear regression for a single ID group.
    Uses SVD algorithm and catches cuSOLVER errors gracefully.
    """
    n_obs = len(df_group)
    if n_obs < 5:
        return cudf.DataFrame()

    # --- 1. Compute weights ---
    doy_col = df_group['doy'].to_cupy()
    dist = cp.abs(doy_col - doy_target)
    dist = cp.minimum(dist, 366 - dist)
    scaled = cp.clip(dist / h, 0, 1)
    weights = (1 - scaled**3)**3

    # --- 2. Prepare features & target on GPU ---
    feats = ['TMIN', 'TD_lag1', 'TD_lag2', 'TMIN_lag1']
    X_gpu = df_group[feats].astype('float32').to_cupy()
    y_gpu = df_group['TD'].astype('float32').to_cupy()

    # --- 3. Fit with SVD ---
    try:
        model = LinearRegression(
            fit_intercept=True, algorithm='svd', output_type='cupy'
        )
        model.fit(X_gpu, y_gpu, sample_weight=weights)
    except Exception as e:
        logging.warning(f"cuSOLVER fit failed for ID={df_group['ID'].iloc[0]} DOY={doy_target}: {e}")
        return cudf.DataFrame()

    # --- 4. Predictions & residuals ---
    preds = model.predict(X_gpu)
    residuals = y_gpu - preds

    # --- 5. Accuracy metrics ---
    Wsum = cp.sum(weights)
    wrss = cp.sum(weights * residuals**2)
    rmse = cp.sqrt(wrss / Wsum) if Wsum > 0 else cp.nan
    y_mean_w = cp.sum(weights * y_gpu) / Wsum if Wsum > 0 else cp.nan
    wtss = cp.sum(weights * (y_gpu - y_mean_w)**2)
    r2 = 1 - (wrss / (wtss + 1e-9)) if wtss > 1e-9 else cp.nan

    # --- 6. Standard errors ---
    n_par = X_gpu.shape[1] + 1
    Xc_gpu = cp.hstack([cp.ones((n_obs, 1), dtype=cp.float32), X_gpu])
    dof = n_obs - n_par

    se_gpu = cp.full(n_par, cp.nan, dtype=cp.float32)
    if dof > 0:
        sigma2 = wrss / dof
        XT_W = Xc_gpu.T * weights
        XTWX = XT_W @ Xc_gpu
        try:
            XTWX_inv = cp.linalg.inv(XTWX)
            se_gpu = cp.sqrt(cp.diag(sigma2 * XTWX_inv))
        except cp.linalg.LinAlgError:
            logging.debug(f"Could not invert X'WX for ID={df_group['ID'].iloc[0]} DOY={doy_target}")

    # --- 7. Build single-row result ---
    coef_arr = model.coef_ if isinstance(model.coef_, cp.ndarray) else model.coef_.to_cupy()
    row = {
        'doy': doy_target,
        'const': float(model.intercept_),
        'TMIN': float(coef_arr[0]), 'TD_lag1': float(coef_arr[1]),
        'TD_lag2': float(coef_arr[2]), 'TMIN_lag1': float(coef_arr[3]),
        'const_se': float(se_gpu[0]), 'TMIN_se': float(se_gpu[1]),
        'TD_lag1_se': float(se_gpu[2]), 'TD_lag2_se': float(se_gpu[3]),
        'TMIN_lag1_se': float(se_gpu[4]), 'r_squared': float(r2), 'rmse': float(rmse)
    }
    return cudf.DataFrame([row])


def gpu_fit_models_with_uncertainty_and_accuracy(
    gdf: cudf.DataFrame, doy_target: int, h: int
) -> cudf.DataFrame:
    """Groups the DataFrame by ID and applies the model fitting function to each group."""
    if gdf.empty:
        return cudf.DataFrame()

    parts = [
        fit_one_model(sub, doy_target, h).assign(ID=id_val)
        for id_val, sub in gdf.groupby('ID')
    ]

    return cudf.concat(parts, ignore_index=True) if parts else cudf.DataFrame()


def gpu_process_batch(chunk_ids: list, doy_target: int, h: int = 11) -> cudf.DataFrame:
    """Reads and processes a chunk of data for a given DOY."""
    try:
        sd, ed = calculate_neighborhood_dates(doy_target, h, 2, YEAR_RANGE)
        filt = [('ID', 'in', chunk_ids)]

        def read_pqt_batch(file_list, columns):
            if not file_list: return cudf.DataFrame()
            return cudf.read_parquet(file_list, filters=filt, columns=columns)

        td_files = find_parquet_files('td', (sd, ed))
        gdf_td = read_pqt_batch(td_files, ['ID', 'FECHA', 'Value']).rename(columns={'Value': 'TD'})

        tmin_files = find_parquet_files('tmin_v1', (sd, ed))
        gdf_tmin = read_pqt_batch(tmin_files, ['ID', 'FECHA', 'Value']).rename(columns={'Value': 'TMIN'})

        if gdf_td.empty or gdf_tmin.empty:
            return cudf.DataFrame()

        df = gdf_td.merge(gdf_tmin, on=['ID', 'FECHA'], how='inner')
        df['doy'] = df['FECHA'].dt.dayofyear
        df = df.sort_values(['ID', 'FECHA'])

        for lag in [1, 2]:
            df[f'TD_lag{lag}'] = df.groupby('ID')['TD'].shift(lag)
        df['TMIN_lag1'] = df.groupby('ID')['TMIN'].shift(1)

        lb, ub = doy_target - h, doy_target + h
        # **Corrected year-wrapping mask**
        if lb < 1:
            mask = (df['doy'] >= (lb + 366)) | (df['doy'] <= ub)
        elif ub > 366:
            mask = (df['doy'] >= lb) | (df['doy'] <= (ub - 366))
        else:
            mask = (df['doy'] >= lb) & (df['doy'] <= ub)

        df_f = df[mask].dropna()
        if df_f.empty:
            return cudf.DataFrame()

        return gpu_fit_models_with_uncertainty_and_accuracy(df_f, doy_target, h)
    except Exception:
        logging.exception(f"Batch processing failed for DOY {doy_target}")
        return cudf.DataFrame()
    finally:
        try:
            cp.get_default_memory_pool().free_all_blocks()
        except Exception:
            pass


def main():
    """Main execution function with pipelined Dask workflow."""
    cluster = None
    client = None
    try:
        logging.info("--- Starting Optimized Pipeline (V6 - Pipelined) ---")
        pg = gpd.read_file(POTATO_GRID_FILE)
        pg["ID"] = pg.get("ID", pg.index)
        all_ids = pg['ID'].unique().tolist()
        logging.info(f"Loaded {len(all_ids)} unique IDs to process.")

        # Use the standard Dask LocalCluster for multi-processing on a single machine
        cluster = LocalCluster(n_workers=GPU_WORKERS, threads_per_worker=4)
        client = Client(cluster)
        logging.info(f"Dask Dashboard: {client.dashboard_link}")

        # Semaphore to control concurrency and prevent OOM errors
        max_concurrent_tasks = GPU_WORKERS * 2  # A safe multiplier
        sem = Semaphore(max_leases=max_concurrent_tasks, name="gpu-process-sem")

        results_dir = os.path.join(BASE_PATH, 'results')
        os.makedirs(results_dir, exist_ok=True)

        # --- Pipelining Logic ---
        all_futures = []
        logging.info("Submitting all tasks for all DOYs to the Dask scheduler...")
        for doy in range(1, 367):
            id_chunks = [all_ids[i:i + GPU_CHUNK_SIZE] for i in range(0, len(all_ids), GPU_CHUNK_SIZE)]
            for chunk in id_chunks:
                # Submit tasks with a wrapper to manage the semaphore
                future = client.submit(
                    lambda ch, d, s: s.acquire() and gpu_process_batch(ch, d, 11),
                    chunk, doy, sem, pure=False
                )
                # Ensure semaphore is released when the task is done
                future.add_done_callback(lambda f, s=sem: s.release())
                all_futures.append((doy, future))

        # Process results as they complete
        daily_results = {}
        progress = tqdm(as_completed([f for _, f in all_futures]), total=len(all_futures), desc="Processing All Tasks")

        for future in progress:
            # Find the original DOY for this completed future
            doy, _ = next(((d, f) for d, f in all_futures if f == future), (None, None))
            if doy is None: continue

            try:
                batch_result_gdf = future.result()
                if batch_result_gdf is not None and not batch_result_gdf.empty:
                    if doy not in daily_results:
                        daily_results[doy] = []
                    daily_results[doy].append(batch_result_gdf.to_pandas())
            except Exception as e:
                logging.error(f"A task for DOY {doy} failed: {e}", exc_info=False)
            finally:
                future.release()

        # Save all results at the end
        logging.info("All tasks completed. Saving results to parquet files...")
        for doy, results_list in sorted(daily_results.items()):
            if results_list:
                final_df = pd.concat(results_list, ignore_index=True)
                output_path = os.path.join(results_dir, f"gpu_models_doy_{doy}.parquet")
                final_df.to_parquet(output_path, engine='pyarrow')
                logging.info(f"SUCCESS: Saved DOY {doy} results ({len(final_df)} models) to {output_path}")

    except Exception as e:
        logging.critical("A critical error occurred in the main function!", exc_info=True)
    finally:
        logging.info("--- Shutting down Dask client and cluster ---")
        if client: client.close()
        if cluster: cluster.close()
        logging.info("--- Pipeline Finished ---")

if __name__ == "__main__":
    main()

