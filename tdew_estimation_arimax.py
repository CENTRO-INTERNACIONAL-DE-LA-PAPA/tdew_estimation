def fit_single_wls_model(df_group, doy_target, h, kernel="Tricube Kernel"):
    """
    Fits just ONE Weighted Least Squares model for a single ID at a specific doy_target.
    This function is much "thinner" and more memory-friendly.
    """
    if df_group.empty or len(df_group) < 15:
        return None

    # Feature engineering for lags is now done on the pre-filtered neighborhood data
    df_group = df_group.sort_values('FECHA')
    df_group['TD_lag1'] = df_group['TD'].shift(1)
    df_group['TD_lag2'] = df_group['TD'].shift(2)
    df_group['TMIN_lag1'] = df_group['TMIN'].shift(1)

    required_cols = ['TD', 'TMIN', 'TD_lag1', 'TD_lag2', 'TMIN_lag1']
    df_group.dropna(subset=required_cols, inplace=True)

    if len(df_group) < 15:
        return None

    # Weight calculation
    distance = np.abs(df_group['doy'] - doy_target)
    distance = np.minimum(distance, 366 - distance)
    scaled_distance = np.clip(distance / h, 0, 1)

    if kernel == "Tricube Kernel":
        weights = (1 - np.abs(scaled_distance)**3)**3
    else: # Gaussian
        weights = np.exp(- (distance**2) / (2 * h**2))

    y = df_group['TD']
    X = df_group[['TMIN', 'TD_lag1', 'TD_lag2', 'TMIN_lag1']]
    X = sm.add_constant(X)

    try:
        wls_model = sm.WLS(y, X, weights=weights).fit()
        # Return the coefficients as a dictionary
        params = wls_model.params.to_dict()
        params['ID'] = df_group['ID'].iloc[0]
        params['doy'] = doy_target
        return pd.DataFrame([params])
    except Exception:
        return None


def main_rearchitected_training():
    """
    Re-architected pipeline that loads data once with Dask and then filters
    lazily inside the loop for maximum efficiency.
    """
    client = Client(n_workers=8, threads_per_worker=4, memory_limit="14GB")
    print(f"Dask dashboard is live at: {client.dashboard_link}")

    potato_grid_file = "/media/ppalacios/Data1/henry_simcast_peru/PotatoZonning/CENAGRO_OnlyPotatoes_Pisco_Altitude.dbf"
    potato_grid = gpd.read_file(potato_grid_file)
    potato_grid["ID"] = potato_grid.get("ID", potato_grid.index)
    all_ids = potato_grid['ID'].tolist()

    optimal_h = 11 # Your optimal bandwidth

    # --- FIX: Load all data ONCE at the beginning using Dask ---
    print("Loading all training data into a master Dask DataFrame...")
    training_date_range = ("1981-01-01", "2016-12-31")
    td_files = find_parquet_files(variable="td", date_range=training_date_range)
    tmin_files = find_parquet_files(variable="tmin", date_range=training_date_range)

    ddf_td = dd.read_parquet(td_files, columns=['ID', 'FECHA', 'Value']).rename(columns={'Value': 'TD'})
    ddf_tmin = dd.read_parquet(tmin_files, columns=['ID', 'FECHA', 'Value']).rename(columns={'Value': 'TMIN'})

    ddf_td = ddf_td[ddf_td['ID'].isin(all_ids)]
    ddf_tmin = ddf_tmin[ddf_tmin['ID'].isin(all_ids)]

    ddf_master = dd.merge(ddf_td, ddf_tmin, on=['FECHA', 'ID'], how='inner')
    ddf_master['FECHA'] = dd.to_datetime(ddf_master['FECHA'])
    ddf_master['doy'] = ddf_master['FECHA'].dt.dayofyear
    print("Master Dask DataFrame created.")

    # --- Main Loop ---
    all_daily_coeffs = []
    for doy_target in tqdm(range(1, 367), desc="Processing Day of Year"):

        # --- 1. Lazily filter the master Dask DataFrame ---
        lower_bound = (doy_target - optimal_h - 1) % 366 + 1
        upper_bound = (doy_target + optimal_h - 1) % 366 + 1

        if lower_bound < upper_bound:
            ddf_slice = ddf_master[(ddf_master.doy >= lower_bound) & (ddf_master.doy <= upper_bound)]
        else: # Wrap-around case
            ddf_slice = ddf_master[(ddf_master.doy >= lower_bound) | (ddf_master.doy <= upper_bound)]

        # --- 2. Run parallel computation on the lazy slice ---
        meta = pd.DataFrame({
            'const': [0.0], 'TMIN': [0.0], 'TD_lag1': [0.0], 'TD_lag2': [0.0],
            'TMIN_lag1': [0.0], 'ID': [0], 'doy': [0]
        })

        # The .compute() call will now trigger the filtering, grouping, and applying in parallel
        coeffs_for_doy = ddf_slice.groupby('ID').apply(
            fit_single_wls_model,
            doy_target=doy_target,
            h=optimal_h,
            meta=meta
        ).compute()

        if not coeffs_for_doy.empty:
            all_daily_coeffs.append(coeffs_for_doy)

    # --- 3. Combine and Save Final Results ---
    if not all_daily_coeffs:
        print("Error: No coefficients were generated. The process failed.")
        client.close()
        return

    print("Combining results from all days...")
    final_results_df = pd.concat(all_daily_coeffs, ignore_index=True)

    print("Training complete. Saving coefficients...")
    output_path = "/media/ppalacios/Data/henry_simcast_peru/results/llr_hybrid_coefficients_v3.parquet"
    final_results_df.to_parquet(output_path, index=False, engine='pyarrow')

    print(f"✅ All models trained. Coefficients saved to: {output_path}")
    client.close()

