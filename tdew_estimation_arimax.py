def load_variable_data(variable: str, date_range: tuple, region_ids: list) -> pd.DataFrame:
    """Load data with partition pruning and row-group filtering"""
    files = find_parquet_files(variable, date_range)
    if not files:
        return pd.DataFrame()

    # Convert dates to numpy datetime64 for filter compatibility
    start_date = pd.Timestamp(date_range[0])
    end_date = pd.Timestamp(date_range[1])

    # Build PyArrow filters for partition pruning and row-group filtering
    filters = [
        ('FECHA', '>=', start_date),
        ('FECHA', '<=', end_date),
        ('ID', 'in', region_ids)
    ]

    # Read with filter pushdown and projection
    df = pd.read_parquet(
        files,
        filters=filters,
        columns=['ID', 'FECHA', 'Value'],
        engine='pyarrow',  # Required for full filter support
        dtype_backend='numpy_nullable'
    )
    if variable == 'prec':
        df['Value'] = df['Value'].fillna(0.0)

    # Post-processing for any remaining filtering
    df['FECHA'] = pd.to_datetime(df['FECHA']).dt.normalize()

    return df.rename(columns={'Value': variable.upper()})

