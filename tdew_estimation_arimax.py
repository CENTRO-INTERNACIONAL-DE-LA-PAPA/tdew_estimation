def find_parquet_files(variable: str, date_range: tuple) -> list:
    """Finds parquet files using the specified naming convention."""
    try:
        start_date, end_date = [pd.Timestamp(d) for d in date_range]
        months = pd.date_range(start_date, end_date, freq='MS').strftime("%Y_%m").unique()
        files = [f for ym in months for f in glob.glob(os.path.join(BASE_PATH, variable, "Outputs", f"{variable}_daily_{ym}.parquet"))]
        if not files:
            logging.warning(f"No parquet files found for {variable} in range {date_range}")
        return sorted(files)
    except Exception as e:
        logging.exception(f"Error in find_parquet_files for {variable}")
        return []

def calculate_neighborhood_dates(doy_target: int, h: int, lag_days: int, year_range: tuple) -> tuple:
    """Calculates the absolute start and end dates needed for the entire dataset."""
    start_year, end_year = year_range
    return pd.Timestamp(f'{start_year}-01-01'), pd.Timestamp(f'{end_year}-12-31')

def gpu_fit_models(gdf: cudf.DataFrame, doy_target: int, h: int) -> cudf.DataFrame:
    try:
        if gdf.empty or len(gdf) < 5:
            return cudf.DataFrame()
        # Group by ID and check if each group has enough data
        group_sizes = gdf.groupby('ID').size()
        valid_ids = group_sizes[group_sizes >= 5].index
        gdf = gdf[gdf['ID'].isin(valid_ids)]
        if gdf.empty:
            return cudf.DataFrame()

        logging.info(f"Fitting models for DOY {doy_target}, {gdf['ID'].nunique()} unique IDs on GPU.")
        doy_col = gdf['doy'].to_cupy()
        distance = cp.abs(doy_col - doy_target)
        distance = cp.minimum(distance, 366 - distance)
        scaled_distance = cp.clip(distance / h, 0, 1)
        weights = (1 - cp.abs(scaled_distance)**3)**3
        features = ['TMIN', 'TD_lag1', 'TD_lag2', 'TMIN_lag1']
        X = gdf[features].astype('float32')
        y = gdf['TD'].astype('float32')
        model = LinearRegression(fit_intercept=True, algorithm='eig', output_type='cudf')
        model.fit(X, y, sample_weight=weights)
        results = cudf.DataFrame({'ID': gdf['ID'],'doy': doy_target,'const': model.intercept_,'TMIN': model.coef_[0],'TD_lag1': model.coef_[1],'TD_lag2': model.coef_[2],'TMIN_lag1': model.coef_[3]})
        final_results = results.drop_duplicates(subset=['ID'])
        logging.info(f"Generated {len(final_results)} models for DOY {doy_target}")
        return final_results
    except Exception as e:
        logging.exception(f"Model fitting error on GPU for DOY {doy_target}")
        return cudf.DataFrame()


def gpu_process_batch(chunk_ids: list, doy_target: int, h: int = 11) -> cudf.DataFrame:
    """Reads files and processes a batch of IDs for a given DOY."""
    try:
        logging.info(f"Processing batch for DOY {doy_target} with {len(chunk_ids)} IDs.")
        start_date, end_date = calculate_neighborhood_dates(doy_target, h, 2, YEAR_RANGE)
        filters = [('ID', 'in', chunk_ids)]

        def read_files_in_batches(file_list, columns, file_batch_size=12):
            df_list = []
            for i in range(0, len(file_list), file_batch_size):
                batch_files = file_list[i:i + file_batch_size]
                try:
                    df_list.append(cudf.read_parquet(batch_files, filters=filters, columns=columns))
                except Exception as e:
                    logging.warning(f"Could not read file batch starting with {batch_files[0]}, skipping. Error: {e}")
            return cudf.concat(df_list, ignore_index=True) if df_list else cudf.DataFrame()

        td_files = find_parquet_files('td', (start_date, end_date))
        gdf_td = read_files_in_batches(td_files, ['ID', 'FECHA', 'Value']).rename(columns={'Value': 'TD'})

        tmin_files = find_parquet_files('tmin', (start_date, end_date))
        gdf_tmin = read_files_in_batches(tmin_files, ['ID', 'FECHA', 'Value']).rename(columns={'Value': 'TMIN'})

        if gdf_td.empty or gdf_tmin.empty: return cudf.DataFrame()

        gdf = gdf_td.merge(gdf_tmin, on=['ID', 'FECHA'], how='inner')
        del gdf_td, gdf_tmin

        if gdf.empty: return cudf.DataFrame()

        gdf['doy'] = gdf['FECHA'].dt.dayofyear
        gdf = gdf.sort_values(['ID', 'FECHA'])

        for lag in [1, 2]:
            gdf[f'TD_lag{lag}'] = gdf.groupby('ID')['TD'].shift(lag)
        gdf['TMIN_lag1'] = gdf.groupby('ID')['TMIN'].shift(1)

        lb, ub = doy_target - h, doy_target + h
        mask = (gdf['doy'] >= (lb + 366)) | (gdf['doy'] <= ub) if lb < 1 else \
               (gdf['doy'] >= lb) | (gdf['doy'] <= (ub - 366)) if ub > 366 else \
               (gdf['doy'] >= lb) & (gdf['doy'] <= ub)

        gdf_filtered = gdf[mask].dropna()
        del gdf

        if gdf_filtered.empty: return cudf.DataFrame()

        return gpu_fit_models(gdf_filtered, doy_target, h)

    except Exception as e:
        logging.exception(f"Batch processing failed for DOY {doy_target}")
        return cudf.DataFrame()
    finally:
        try:
            cp.get_default_memory_pool().free_all_blocks()
        except Exception:
            pass

def main():
    cluster = None
    client = None
    try:
        logging.info("--- Starting Targeted Model Regeneration ---")
        potato_grid = gpd.read_file(POTATO_GRID_FILE)
        potato_grid["ID"] = potato_grid.get("ID", potato_grid.index)
        all_ids = potato_grid['ID'].unique().tolist()
        logging.info(f"Loaded {len(all_ids)} unique IDs to process.")

        logging.info("Initializing Dask CUDA Cluster...")
        cluster = LocalCUDACluster(n_workers=GPU_WORKERS, threads_per_worker=4, device_memory_limit="11.5GB", local_directory=SCRATCH_DIR, rmm_pool_size="10.5GB")
        client = Client(cluster)
        logging.info(f"Dask Dashboard: {client.dashboard_link}")

        results_dir = os.path.join(BASE_PATH, "results")

        # --- MODIFICATION: Define and loop over only the days that need to be fixed ---
        DOYS_TO_FIX = [13, 54, 65, 82, 90, 114, 130, 134, 158, 159, 160, 161, 192, 202, 212, 228, 229, 255, 270, 279, 295, 308, 309, 330, 350, 363]

        for doy in DOYS_TO_FIX:
            logging.info(f"\n{'='*50}\nFIXING models for DOY: {doy}\n{'='*50}")
            start_time_doy = time.time()
            id_chunks = [all_ids[i:i + GPU_CHUNK_SIZE] for i in range(0, len(all_ids), GPU_CHUNK_SIZE)]
            futures = [client.submit(gpu_process_batch, chunk, doy_target=doy, h=11) for chunk in id_chunks]

            daily_results = []
            progress = tqdm(as_completed(futures), total=len(futures), desc=f"Processing DOY {doy}")
            for future in progress:
                try:
                    batch_result_gdf = future.result()
                    if batch_result_gdf is not None and not batch_result_gdf.empty:
                        daily_results.append(batch_result_gdf.to_pandas())
                except Exception as e:
                    logging.error(f"A task failed with error: {e}", exc_info=True)
                finally:
                    future.release()

            duration_doy = time.time() - start_time_doy
            logging.info(f"Finished processing DOY {doy} in {duration_doy:.2f} seconds.")

            if daily_results:
                final_df = pd.concat(daily_results, ignore_index=True)
                output_path = os.path.join(results_dir, f"gpu_models_doy_{doy}.parquet")
                final_df.to_parquet(output_path, engine='pyarrow')
                logging.info(f"SUCCESS: Regenerated DOY {doy} results ({len(final_df)} models) to {output_path}")
            else:
                logging.warning(f"No results were generated for DOY {doy}.")

            del futures, daily_results
            gc.collect()

    except Exception as e:
        logging.critical("A critical error occurred in the main function!", exc_info=True)
    finally:
        logging.info("--- Shutting down Dask client and cluster ---")
        if client: client.close()
        if cluster: cluster.close()
        logging.info("--- Pipeline Finished ---")

if __name__ == "__main__":
    main()

